#' @rdname diagnosticmeta_test_datasets
#' @title Standard Diagnostic Meta-Analysis Dataset
#' @description
#' Standard diagnostic test accuracy meta-analysis data with 20 studies,
#' realistic sensitivity/specificity values, moderate heterogeneity, and
#' continuous covariates for meta-regression.
#'
#' @format A data frame with 20 observations and 7 variables:
#' \describe{
#'   \item{study}{Character. Unique study identifier (Study_1 to Study_20)}
#'   \item{true_positives}{Numeric. Number of true positive results (1-100)}
#'   \item{false_positives}{Numeric. Number of false positive results (1-120)}
#'   \item{false_negatives}{Numeric. Number of false negative results (1-40)}
#'   \item{true_negatives}{Numeric. Number of true negative results (1-140)}
#'   \item{year}{Numeric. Publication year (2015-2024)}
#'   \item{quality_score}{Numeric. Study quality score (1-10)}
#' }
#'
#' @details
#' This dataset simulates a meta-analysis of diagnostic test accuracy studies with:
#' - Realistic sensitivity: 60-99% (mean ~85%)
#' - Realistic specificity: 70-99% (mean ~90%)
#' - Moderate between-study heterogeneity
#' - Realistic sample size variation (50-240 per study)
#' - Correlation: Higher quality studies have larger sample sizes
#' - Minimal zero cells (2 studies) for realistic modeling
#'
#' Typical use: AI algorithm validation, biomarker diagnostic accuracy,
#' IHC marker validation in pathology.
#'
#' @examples
#' # Load the data
#' data(diagnosticmeta_test)
#'
#' # View structure
#' str(diagnosticmeta_test)
#'
#' # Basic meta-analysis
#' result <- diagnosticmeta(
#'   data = diagnosticmeta_test,
#'   study = "study",
#'   true_positives = "true_positives",
#'   false_positives = "false_positives",
#'   false_negatives = "false_negatives",
#'   true_negatives = "true_negatives"
#' )
#'
#' # Meta-regression with year as covariate
#' result_year <- diagnosticmeta(
#'   data = diagnosticmeta_test,
#'   study = "study",
#'   true_positives = "true_positives",
#'   false_positives = "false_positives",
#'   false_negatives = "false_negatives",
#'   true_negatives = "true_negatives",
#'   covariate = "year",
#'   meta_regression = TRUE
#' )
"diagnosticmeta_test"


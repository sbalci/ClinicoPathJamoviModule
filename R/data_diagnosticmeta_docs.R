#' @rdname diagnosticmeta_test_datasets
#' @title Zero-Cell Diagnostic Meta-Analysis Dataset
#' @description
#' Diagnostic test accuracy meta-analysis data with intentional zero cells
#' in several studies, designed for testing zero-cell correction methods
#' (none, constant, treatment_arm, empirical).
#'
#' @format A data frame with 20 observations and 7 variables:
#' \describe{
#'   \item{study}{Character. Unique study identifier (Study_1 to Study_20)}
#'   \item{true_positives}{Numeric. Number of true positive results}
#'   \item{false_positives}{Numeric. Number of false positive results (zero in studies 1, 5, 10)}
#'   \item{false_negatives}{Numeric. Number of false negative results (zero in studies 2, 7)}
#'   \item{true_negatives}{Numeric. Number of true negative results}
#'   \item{year}{Numeric. Publication year (2015-2024)}
#'   \item{quality_score}{Numeric. Study quality score (1-10)}
#' }
#'
#' @details
#' This dataset includes intentional zero cells to test:
#' - Model-based zero-cell handling (bivariate model, recommended)
#' - Constant correction (+0.5 to all cells)
#' - Treatment-arm correction (add only to zero cells)
#' - Empirical correction (study-specific)
#'
#' Zero cells are common in diagnostic meta-analysis when:
#' - Studies have perfect sensitivity or specificity
#' - Sample sizes are small
#' - Test threshold is extreme
#'
#' @examples
#' # Load the data
#' data(diagnosticmeta_test_zeros)
#'
#' # Test model-based approach (recommended)
#' result_none <- diagnosticmeta(
#'   data = diagnosticmeta_test_zeros,
#'   study = "study",
#'   true_positives = "true_positives",
#'   false_positives = "false_positives",
#'   false_negatives = "false_negatives",
#'   true_negatives = "true_negatives",
#'   zero_cell_correction = "none"
#' )
#'
#' # Test constant correction
#' result_constant <- diagnosticmeta(
#'   data = diagnosticmeta_test_zeros,
#'   study = "study",
#'   true_positives = "true_positives",
#'   false_positives = "false_positives",
#'   false_negatives = "false_negatives",
#'   true_negatives = "true_negatives",
#'   zero_cell_correction = "constant"
#' )
"diagnosticmeta_test_zeros"

